const express = require("express");
const router = express.Router();

// âœ… Controllers
const {
  uploadStory,
  getAllStories,
  viewStory,
  deleteStory,
} = require("../controllers/storyController");

// âœ… Middlewares
const auth = require("../middleware/auth");         // ğŸ” JWT Auth Middleware
const upload = require("../middleware/multer");     // â˜ï¸ Multer + Cloudinary setup

// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// ğŸ“¤ Upload Story (POST)
// URL: /api/story/upload
// Auth: âœ… Required
// Body: form-data { story: File, caption: Text }
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
router.post("/upload", auth, upload.single("story"), uploadStory);

// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// ğŸ“¥ Get All Stories (GET)
// URL: /api/story/all
// Auth: âœ… Required
// Info: Expired stories auto-deleted via TTL in schema
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
router.get("/all", auth, getAllStories);

// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// ğŸ‘ï¸ Mark Story as Viewed (PUT)
// URL: /api/story/view/:id
// Auth: âœ… Required
// Info: Adds userId to story.views[]
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
router.put("/view/:id", auth, viewStory);

// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
// âŒ Delete Story (DELETE)
// URL: /api/story/delete/:id
// Auth: âœ… Required (only owner)
// â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
router.delete("/delete/:id", auth, deleteStory);

// âœ… Export Router
module.exports = router;
